#Set in python

#Create Set using curly braces
numbers = {2, 4, 8, 10, 12}
print(numbers)
print(type(numbers))


#Create set using Set constructor
numbers1 = set({2, 4, 8, 10, 12})
print(numbers1)
print(type(numbers1))

numbers2 = set([2, 4, 8, 10, 12])
print(numbers2)
print(type(numbers2))

numbers3 = set((2, 4, 8, 10, 12))
print(numbers3)
print(type(numbers3))
